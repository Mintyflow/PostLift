  const thisMonthKey=getMonthKey();
  const monthlyUsed=user
    ?(user.usageMonth===thisMonthKey?(user.postsUsedMonth||0):0)
    :getGuestUsed();
  const used=monthlyUsed; // used this month (resets on 1st of each month)
  const rem=isPaid?Infinity:FREE_LIMIT-used;
  const canGen=!!(topic.trim()&&industry);

  async function generate(){
    if(!canGen) return;
    if(rem<=0){ setShowUpgrade(true); return; }
    const hourly=checkHourlyLimit();
    if(!hourly.allowed){
      setErr("You have generated "+hourly.used+" posts this hour. Please wait a few minutes before generating more. This protects the service for everyone.");
      return;
    }
    setLoading(true); setErr(null); setPosts({});
    const cfg={topic:topic.trim(),industry,role,goal,audience,keyMsg,tone,uk:isUK};
    const results=await Promise.allSettled(
      STYLES.map(async (s,i)=>{
        const text=await genPost(cfg,s.id,0,i*150);
        setPosts(p=>({...p,[s.id]:text||"__error__"}));
        return s.id;
      })
    );
    const ok=results.filter(r=>r.status==="fulfilled");
    const fail=results.map((r,i)=>r.status==="rejected"?i:-1).filter(i=>i>=0);
    if(ok.length===0){
      setErr("Could not connect to AI. Please check your internet and try again.");
      setPosts(null);
    } else if(fail.length>0){
      fail.forEach(i=>{ const sid=STYLES[i].id; setPosts(p=>({...p,[sid]:"__error__"})); });
    }
    if(ok.length>0){
      recordHourlyGen();
      if(user) updateUsage();
      else recordGuestGen(); // track guest monthly usage
    }
    // Show email capture banner ~2s after posts load (only if not already subscribed)
    if(ok.length>0){
      const emails=JSON.parse(localStorage.getItem("pl_emails")||"[]");
      const alreadySubscribed = user?.email && emails.find(e=>e.email===user.email);
      if(!alreadySubscribed) setTimeout(()=>setShowEmailBanner(true), 2000);
    }
    setLoading(false);
    // Silent auto-retry: failed posts get one quiet extra attempt 800ms later
    const failedStyles=results.map((r,i)=>r.status==="rejected"?STYLES[i]:null).filter(Boolean);
    if(failedStyles.length>0&&ok.length>0){
      setTimeout(async()=>{
        for(const s of failedStyles){
          try{
            const text=await genPost(cfg,s.id,0,0);
            if(text) setPosts(p=>({...p,[s.id]:text}));
          }catch(e){}
        }
      },800);
    }
  }

