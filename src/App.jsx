    setPosts(p=>({...p,[styleId]:null}));
    try{
      const text=await genPost(cfg,styleId,0);
      setPosts(p=>({...p,[styleId]:text||"__error__"}));
    }catch(e){
      setPosts(p=>({...p,[styleId]:"__error__"}));
    }
