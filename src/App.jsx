/* ── Post generation — non-streaming (direct API) ──────────────────────────────────
   Uses Haiku: 5-6x faster than Sonnet for short posts, near-identical quality
   at 80-200 words. max_tokens tight at 450 (posts are ~100-260 tokens).
   Streaming needs a backend proxy to work reliably — browser-direct SSE is
   blocked by CORS in sandboxed environments. Enable in /api/generate after launch.
   ────────────────────────────────────────────────────────────────────────── */
/* staggerOffset staggers the 5 parallel calls so they don't all hit the API
   simultaneously (primary fix for rate-limit failures).
   MAX=2: one silent auto-retry then show error. Max cost: 2 calls per post. */
async function genPost(cfg,styleId,attempt,staggerOffset){
  if(attempt===undefined) attempt=0;
  if(staggerOffset===undefined) staggerOffset=0;
  const MAX=2;
  const retryDelay=1600;
  try{
    if(attempt===0&&staggerOffset>0) await new Promise(r=>setTimeout(r,staggerOffset));
    if(attempt>0) await new Promise(r=>setTimeout(r,retryDelay));
    const res=await fetch("/api/generate",{
      method:"POST",
      headers:{"Content-Type":"application/json"},
      body:JSON.stringify({
        model:"claude-haiku-4-5-20251001",
        max_tokens:450,
        messages:[{role:"user",content:buildPrompt(cfg,styleId)}]
      })
    });
    if((res.status===429||res.status>=500)&&attempt<MAX-1) return genPost(cfg,styleId,attempt+1,0);
    if(!res.ok) throw new Error("API "+res.status);
    const d=await res.json();
    const text=(d.content&&d.content[0]&&d.content[0].text||"").trim();
    if(!text&&attempt<MAX-1) return genPost(cfg,styleId,attempt+1,0);
    return text;
  }catch(e){
    if(attempt<MAX-1) return genPost(cfg,styleId,attempt+1,0);
    throw e;
  }
}

